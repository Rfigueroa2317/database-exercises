Indexes

primary keys;
    . Must be on the table independently
    . Highlander rule (there can only be one)
    . Optimizes search
    . Can be more than 1 column

Foreign keys;
    . Must primarily exist on another table
    . Used so our table doesnt have to maintain out-of-scope something
    . Tables and fk are dependent on Fks pk table

Unique;
    . Can be any column
    . Completely index of other tables
    . Gives perform boost

Clustered index;
    . More than 1 column
    . Can be a primariy key
    . Must be unique
    . Can be referenced as an Fk